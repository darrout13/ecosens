                <p>La misión de este proyecto es proporcionar a la comunidad de IUB (nombre de la institución o ubicación específica) un sistema integral de monitoreo continuo en tiempo real de la temperatura, humedad y niveles de contaminación del aire. A través de la combinación de hardware y software, buscamos ofrecer una solución accesible y de bajo costo que permita la identificación oportuna de fuentes específicas de contaminación, la toma de decisiones informadas y la implementación eficaz de medidas correctivas. Nuestra misión es mejorar la calidad de vida de la comunidad al proporcionar información precisa y accesible sobre la calidad del aire que respiran.</p>
